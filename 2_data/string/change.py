
strs = "string".upper() # 大写
strs = "String".lower() # 小写
strs = "String".title() # 首字母大写
strs = "String".capitalize() # 第一个字母大写
strs = "String".swapcase() # 大小写互换
strs = "String".casefold() # 小写
strs = "String".encode() # 编码 可选编码格式（比如utf-8）、错误处理方式（比如ignore）
strs = "String".encode().decode() # 编码解码 可选解码格式（比如utf-8）、错误处理方式（比如ignore）
